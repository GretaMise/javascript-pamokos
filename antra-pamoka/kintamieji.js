// Kintamieji (variables)
// Kintamasis - atminties vieta, kurioje saugoma informacija

// 1. let - kintamasis, kurio reiksme gali buti keiciama

let amzius = 26;

console.log('Greta' + ' ' + 'Mano amzius: ' + amzius);

amzius = 27;

console.log('Greta' + ' ' + 'Mano amzius: ' + amzius);

// 2. const - kintamasis, kurio reiksmes keisti negalima

const vardas = 'Jonas';

// Taip daryt negalima, konstanta nera redaguojama (jei neaisku, atsikoemntuok)>>>
// vardas = 'Petras';

console.log('Mano vardas:' + vardas);

// 3. var - senas budas kurti kintamuosius; dabar nenaudojamas, nes yra neprognozuojamas.

var pavarde = 'Petraitis';

pavarde = 'Jonaitis';

console.log('Mano pavarde' + pavarde);
